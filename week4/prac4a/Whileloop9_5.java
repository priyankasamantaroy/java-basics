//3.	Write a program, using a while loop, that will display the numbers -9 to 5 on screen.

public class Whileloop9_5
{
   public static void main(String args[])
   {
   //declair and initialised variable
      int counter =-9;
   
   //while the condition is true
      while (counter<=5)
      {
   //loop body
      System.out.println(counter);
      counter++;
      }
   }
}
      
      
