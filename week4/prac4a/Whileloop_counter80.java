//2.	Write a program, using a while loop, that will display the numbers 1 to 80 on screen.

public class Whileloop_counter80
{
   public static void main(String args[])
   {
      //declair variable
      int counter=1;  //intialising and assigning variables 
      
      //while the condi is true
      while (counter<=80)
      {
         //loop blody
         System.out.println(counter);
         counter++;
      }
   }
}
    