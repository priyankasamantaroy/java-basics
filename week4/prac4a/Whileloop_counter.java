//1.	Write a program, using a while loop, that will display the numbers 1, 2, 3, 4…10 on screen

public class Whileloop_counter
{
   public static void main(String args[])
   {
      //declair variable
      int counter=1;  //intialising and assigning variables 
      
      //condition for loop
      while (counter<=10)//while the condition is true
      {
         //loop blody
         System.out.println(counter);
         counter++;
      }
   }
}
    